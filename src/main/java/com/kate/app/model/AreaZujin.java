package com.kate.app.model;

public class AreaZujin {
	private String year;
	private int rate;
	public AreaZujin(String year,int rate){
		this.year=year;
		this.rate=rate;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public int getRate() {
		return rate;
	}
	public void setRate(int rate) {
		this.rate = rate;
	}
	
}
